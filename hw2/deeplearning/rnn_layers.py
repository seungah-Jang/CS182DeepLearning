def sigmoid(x):
    """
    A numerically stable version of the logistic sigmoid function.
    """
    pos_mask = (x >= 0)
    neg_mask = (x < 0)
    z = np.zeros_like(x)
    z[pos_mask] = np.exp(-x[pos_mask])
    z[neg_mask] = np.exp(x[neg_mask])
    top = np.ones_like(x)
    top[neg_mask] = z[neg_mask]
    return top / (1 + z)


def lstm_step_forward(x, prev_h, prev_c, Wx, Wh, b):
    """
    Forward pass for a single timestep of an LSTM.

    The input data has dimension D, the hidden state has dimension H, and we use
    a minibatch size of N.

    Inputs:
    - x: Input data, of shape (N, D)
    - prev_h: Previous hidden state, of shape (N, H)
    - prev_c: previous cell state, of shape (N, H)
    - Wx: Input-to-hidden weights, of shape (D, 4H)
    - Wh: Hidden-to-hidden weights, of shape (H, 4H)
    - b: Biases, of shape (4H,)

    Returns a tuple of:
    - next_h: Next hidden state, of shape (N, H)
    - next_c: Next cell state, of shape (N, H)
    - cache: Tuple of values needed for backward pass.
    """
    next_h, next_c, cache = None, None, None
    #############################################################################
    # TODO: Implement the forward pass for a single timestep of an LSTM.        #
    # You may want to use the numerically stable sigmoid implementation above.  #
    #############################################################################
    H = prev_h.shape[1]
    
    # 1. 전체 gate를 한 번에 계산
    a = x @ Wx + prev_h @ Wh + b  # shape: (N, 4H)

    # 2. 분할: input, forget, output gate + block input
    ai = a[:, :H]
    af = a[:, H:2*H]
    ao = a[:, 2*H:3*H]
    ag = a[:, 3*H:]

    # 3. 활성화 함수 적용
    i = sigmoid(ai)   # input gate
    f = sigmoid(af)   # forget gate
    o = sigmoid(ao)   # output gate
    g = np.tanh(ag)   # block input

    # 4. 셀 상태 업데이트
    next_c = f * prev_c + i * g

    # 5. hidden state 업데이트
    next_h = o * np.tanh(next_c)

    # 6. 역전파를 위한 값 저장
    cache = (x, prev_h, prev_c, Wx, Wh, b, i, f, o, g, next_c)
    ##############################################################################
    #                               END OF YOUR CODE                             #
    ##############################################################################

    return next_h, next_c, cache


def lstm_step_backward(dnext_h, dnext_c, cache):
    """
    Backward pass for a single timestep of an LSTM.

    Inputs:
    - dnext_h: Gradients of next hidden state, of shape (N, H)
    - dnext_c: Gradients of next cell state, of shape (N, H)
    - cache: Values from the forward pass

    Returns a tuple of:
    - dx: Gradient of input data, of shape (N, D)
    - dprev_h: Gradient of previous hidden state, of shape (N, H)
    - dprev_c: Gradient of previous cell state, of shape (N, H)
    - dWx: Gradient of input-to-hidden weights, of shape (D, 4H)
    - dWh: Gradient of hidden-to-hidden weights, of shape (H, 4H)
    - db: Gradient of biases, of shape (4H,)
    """
    dx, dh, dc, dWx, dWh, db = None, None, None, None, None, None
    #############################################################################
    # TODO: Implement the backward pass for a single timestep of an LSTM.       #
    #                                                                           #
    # HINT: For sigmoid and tanh you can compute local derivatives in terms of  #
    # the output value from the nonlinearity.                                   #
    #############################################################################
    
    x, prev_h, prev_c, Wx, Wh, b, i, f, o, g, next_c = cache
    N, H = dnext_h.shape

    dtanh_next_c = dnext_h * o * (1 - np.tanh(next_c)**2)
    dnext_c_total = dnext_c + dtanh_next_c  # accumulate from both paths

    # Gradients w.r.t gates
    di = dnext_c_total * g
    df = dnext_c_total * prev_c
    do = dnext_h * np.tanh(next_c)
    dg = dnext_c_total * i
    dprev_c = dnext_c_total * f

    # Gradients w.r.t gate pre-activations
    dai = di * i * (1 - i)
    daf = df * f * (1 - f)
    dao = do * o * (1 - o)
    dag = dg * (1 - g**2)

    # Concatenate to get da
    da = np.hstack((dai, daf, dao, dag))  # shape: (N, 4H)

    # Gradients w.r.t inputs and weights
    dx = da @ Wx.T
    dWx = x.T @ da
    dprev_h = da @ Wh.T
    dWh = prev_h.T @ da
    db = np.sum(da, axis=0)


    ##############################################################################
    #                               END OF YOUR CODE                             #
    ##############################################################################

    return dx, dprev_h, dprev_c, dWx, dWh, db


